export /**
 * Menghasilkan data tahun dari tahun tertentu hingga saat ini.
 * @param {number} startYear - Tahun mulai (misal: 1990)
 * @returns {Array} Array objek {label, value}
 */
const generateYearData = (startYear:number) => {
  const currentYear = new Date().getFullYear()
  const years = []

  // Looping mundur dari tahun sekarang ke tahun mulai
  for (let year = currentYear; year >= startYear; year--) {
    years.push({
      label: year.toString(),
      value: year.toString(),
    })
  }

  return years
}
